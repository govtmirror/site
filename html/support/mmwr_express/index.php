<span class="hang"><span class="qa">Q:</span> How do I get the MMWR Express app?</span>
<br/>
<span class="hang"><span class="qa">A:</span> You can <a href="#">download the app</a> for your iPhone, iPod touch, or iPad from Apple's App Store — free of charge. </span>
<br/><br/>
<span class="hang"><span class="qa">Q:</span> Is any information about me collected or stored on the app?</span><br/>
<span class="hang"><span class="qa">A:</span>  No</span>

<br/><br/>
<span class="hang"><span class="qa">Q:</span> How is the "Articles" tab different from the "Search" tab?</span><br/> 
<span class="hang"><span class="qa">A:</span> The "Articles" tab allows access to an article's blue summary box information by specific MMWR article. Articles are grouped by MMWR issue, and issues are listed in sequential order (most recent publication date first). The "Search" tab allows users to search for blue summary box information via keywords related to a subject (e.g., influenza).</span>
<br/><br/>
<span class="hang"><span class="qa">Q:</span> What if I want to view the full article, not just the blue summary box information?</span><br/> 
<span class="hang"><span class="qa">A:</span> Navigate to the list of articles (either via the "Articles" tab, or by keyword via the "Search" tab). There will be a "More Info" icon (i) next to each article. Tap it, and a popover (on the iPad) or a screen (on the iPhone) will appear. Tap the "View Full Article" button on the popover/screen. It will present the full article text.</span>

<br/><br/>
<span class="hang"><span class="qa">Q:</span> How do I get the most recent MMWR issue(s) and subject information?</span><br/> 
<span class="hang"><span class="qa">A:</span> On the "Articles" tab, navigate to the list of articles. Pull down (with your finger) to refresh the content. You will see a spinning graphic while the new content is loading. </span>
